int armPotInit = 0;
void moveArmTo(int height)
{
	int currHeight = -1*(SensorValue[armPot]-armPotInit);
	if(	currHeight < height )
	{
		while(currHeight < height)
		{
			motor[riTower] = 127;
			motor[roTower] = 127;
			motor[liTower] = 127;
			motor[loTower] = 127;
			currHeight = -1*(SensorValue[armPot]-armPotInit);
			writeDebugStreamLine("%f<%f", currHeight, height);
		}
	}
	else
	{
		while(currHeight > height)
		{
			motor[riTower] = -127;
			motor[roTower] = -127;
			motor[liTower] = -127;
			motor[loTower] = -127;
			currHeight = -1*(SensorValue[armPot]-armPotInit);
		}
	}
	motor[riTower] = 0;
	motor[roTower] = 0;
	motor[liTower] = 0;
	motor[loTower] = 0;
}

//Up the arm, open the gripper and down the arm
void armThrow()
{
	int armPotVal = SensorValue[armPot];
	bool throw = true;
	//up the arm
	while (-(armPotVal - armPotInit) < 2200)
	{
		writeDebugStreamLine("Condición %d > 2000", (armPotVal - armPotInit));
		motor[riTower] = 127;
		motor[roTower] = 127;
		motor[liTower] = 127;
		motor[loTower] = 127;
		delay(50);
		armPotVal = SensorValue[armPot];

		if(-(armPotVal - armPotInit) > 2000 && throw)
		{
			//Open gipper
			gripperAction(1);
			throw = false;
		}
	}

	//Down the arm
	while (-(armPotVal - armPotInit) > 5)
	{
		writeDebugStreamLine("Condición %d < 5", (armPotVal - armPotInit));
		motor[riTower] = -127;
		motor[roTower] = -127;
		motor[liTower] = -127;
		motor[loTower] = -127;
		delay(50);
		armPotVal = SensorValue[armPot];
	}

	motor[riTower] = 0;
	motor[roTower] = 0;
	motor[liTower] = 0;
	motor[loTower] = 0;
}

